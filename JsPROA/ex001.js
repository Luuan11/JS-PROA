//Faça um programa que leia um valor informado pelo usuário e diga se o valor informado é positivo, negativo ou zero.

let n1 = Number(prompt('Digite o primeiro valor: '));
        let n2 = Number(prompt('Digite o segundo valor: '));

        if (n1 > 0 ){
            document.write(`O primeiro valor: ${n1} é Positivo <br>`);
        }else {
            n1 < 0
            document.write(`O primeiro valor: ${n1} é Negativo <br>`);
        }
        
        if (n2 > 0 ){
            document.write(`O segundo valor: ${n2} é Positivo <br>` );
        }else{
            n2 < 0
            document.write(`O segundo valor: ${n2} é Negativo <br>`);
        }